function apertaBotao() {
  let tituloFilme = document.querySelector(".titulo").textContent;
  let salvaTitulo = () => {
    localStorage.setItem("tituloFilme", tituloFilme);
  };
  salvaTitulo();
  window.location.href = "/src/html/info.html";
}

